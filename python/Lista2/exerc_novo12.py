n1 = int(input('Digite um numero '))
n2 = int(input('Digite um numero '))

if n1 > n2:
    novo = n1 - n2
    print(f'O primeiro número é maior que o segundo: {n1}')
    print(f'A diferença entre os numeros foram de: {novo}')
elif n1 > n2:
    novo = n2 - n1
    print(f'O primeiro número é maior que o segundo: {n2}')
    print(f'A diferença entre os numeros foram de: {novo}')
else:
    print('Os números são iguais')    










